#include <stdio.h>

int main()
{
    // this programm is to find the area of circle & volume of cylinder
    float radius = 2.4;
    float height = 5.3;
    printf("The area of the circle is : %f\n", 3.14 * radius * radius);
    printf("The volume of the cylinder is %f:\n ", 3.14 * (radius * radius) * height);

    return 0;
}
